
//예제 : 자바 문법에서 사용되는 상수리터럴 종류 그냥 출력 해보기1 
public class Data01 {
	public static void main(String[] args) {
		//(1) 정수 : 소수점이 없는 숫자
		System.out.println(1);
		// (2) 실수 : 소수점이 없는 숫자
		System.out.println(1.5);
		//(3) 문자  : ''단일 따옴표로 묶은 하나의 데이터
		System.out.println('a');
		System.out.print("a \n"); // a문자 출력후 \n 확장특수문자를 통해 한줄 줄바꿈 처리

		//(4) 논리 : true 참 또는 false 거짓의 데이터
		System.out.println(true);

	}

}
