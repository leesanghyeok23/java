
//예제 : 더 다양한 종류의 상수리터럴 출력 해보기 2
public class Data02 {

	public static void main(String[] args) {
		//(1) long형 정수형태의 상수 : 숫자 끝에 L 혹은 ㅣ접미사를 붙여 표현한 정수
		System.out.println(1L); //1
		//(2) float형 실수형태의 상수 : 숫자 끝에 F 혹은 f접미사를 붙여 표현한 실수 
		System.out.println(1.5f);//1.5
		//(3) 레퍼런스형 중에서 문자열 상수 : 이 중 따옴표 "" 로 묶어준 데이터
		System.out.println("abc");//"abc"
	}

}
